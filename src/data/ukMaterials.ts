export const getMaterialsByCategory = (category: MaterialCategory): Material[] => {
  return ukMaterialDatabase.filter(m => m.category === category);
};

export const getMaterialById = (id: string): Material | undefined => {
  return ukMaterialDatabase.find(m => m.id === id);
};

// Custom materials storage (in-memory for now, could be localStorage)
let customMaterials: Material[] = [];

export const addCustomMaterial = (material: Material): void => {
  customMaterials.push(material);
};

export const getCustomMaterials = (): Material[] => {
  return customMaterials;
};

export const getAllMaterials = (): Material[] => {
  return [...ukMaterialDatabase, ...customMaterials];
};
